/// [HFBreakpoints] defines the screen width breakpoints for responsive design.
/// 
/// These values help determine the layout strategy across different device categories.
class HFBreakpoints {
  /// Small mobile devices (e.g., iPhone SE) - 320.0
  static const double mobileSmall = 320.0;
  
  /// Standard mobile devices - 375.0
  static const double mobile = 375.0;
  
  /// Large mobile devices (e.g., iPhone Pro Max) - 414.0
  static const double mobileLarge = 414.0;
  
  /// Tablets (e.g., iPad) - 768.0
  static const double tablet = 768.0;
  
  /// Desktop monitors - 1024.0
  static const double desktop = 1024.0;
}
